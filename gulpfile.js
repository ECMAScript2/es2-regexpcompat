const gulp   = require('gulp'),
      closureCompiler = require('google-closure-compiler').gulp(),
      fs     = require( 'fs' );

gulp.task( 'js', gulp.series(
  function(){
      return gulp.src(
          [
                './lib/index.es6.js'
          ]
      ).pipe(
          closureCompiler(
              {
                  // externs           : externs,
                  // define            : [],
                  // compilation_level : 'ADVANCED',
                  // compilation_level : 'WHITESPACE_ONLY',
                  formatting        : 'PRETTY_PRINT',
                  warning_level     : 'VERBOSE',
                  language_in       : 'ECMASCRIPT_2020',
                  language_out      : 'ECMASCRIPT5',
                  output_wrapper    : '(function(){\n%output%\n})()',
                  js_output_file    : 'ReRE.es5.js'
              }
          )
      ).pipe( gulp.dest( 'lib' ) );
  }
) );

var dontEditMessage = '// THIS SCRIPT IS GENERATED BY "##". DO NOT EDIT!\n\n';

gulp.task( 'data', gulp.series(
    function( cb ){
        var fileName = './tools.js/2_generateUnicodeFoldMap.js';
        // Case folding
        fs.writeFileSync(
            './src.js/2_data.generated/1_unicodeFoldMap.generated.js',
            dontEditMessage.replace( '##', fileName ) + require( fileName )()
        );
        cb();
    },
    function( cb ){
        // category
        cb();
    },
    function( cb ){
        // property
        cb();
    },
    function( cb ){
        // script
        cb();
    },
    function( cb ){
        // legacy
        cb();
    },
    function( cb ){
        // charSetUnicode
        cb();
    }
) );
